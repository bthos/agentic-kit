#!/usr/bin/env bash
# Auto-generates eval-set entries from .agentic-kit-artefacts/archive/<feature>/.
# Each archived feature with both spec.md and handoff-log.md becomes an
# eval-set/<feature-id>.md file containing a list of (requirement, expected)
# pairs. Existing files are NOT modified (per program.md invariants).
#
# Override the artefacts directory with $ARTEFACTS_DIR.
#
# Usage:  agentic-kit/autoresearch/tools/build-eval-set.sh
# Run from project root.

set -euo pipefail

KIT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
EVAL_DIR="$KIT_DIR/eval-set"
ARCHIVE_DIR="${ARTEFACTS_DIR:-.agentic-kit-artefacts}/archive"

mkdir -p "$EVAL_DIR"

if [ ! -d "$ARCHIVE_DIR" ]; then
  echo "No $ARCHIVE_DIR yet — archive a feature first."
  exit 0
fi

added=0
skipped=0

for dir in "$ARCHIVE_DIR"/*/; do
  [ -d "$dir" ] || continue
  feature_id=$(basename "$dir")
  spec="${dir}spec.md"
  handoff="${dir}handoff-log.md"
  out="$EVAL_DIR/${feature_id}.md"

  if [ ! -f "$spec" ]; then
    skipped=$((skipped+1))
    continue
  fi
  if [ -f "$out" ]; then
    skipped=$((skipped+1))
    continue
  fi

  # Extract acceptance criteria block (best effort: lines after "Acceptance criteria"
  # heading, until next heading). Falls back to the whole spec if no AC section.
  criteria=$(awk '
    BEGIN{cap=0}
    /^#+\s*[Aa]cceptance/ {cap=1; next}
    cap==1 && /^#+\s/ {cap=0}
    cap==1 {print}
  ' "$spec" | sed '/^\s*$/d')

  if [ -z "$criteria" ]; then
    criteria=$(head -n 50 "$spec")
  fi

  # Extract "What was built" line(s) from handoff-log.md if present
  built=""
  if [ -f "$handoff" ]; then
    built=$(grep -E -i 'what was built|implemented:' "$handoff" | head -n 5 || true)
  fi

  {
    echo "# Eval entry — $feature_id"
    echo ""
    echo "_Generated by build-eval-set.sh on $(date '+%Y-%m-%d'). Feature: \`$feature_id\`._"
    echo ""
    echo "## Requirements"
    echo ""
    printf '%s\n' "$criteria"
    echo ""
    echo "## Reference output (what was actually built)"
    echo ""
    if [ -n "$built" ]; then
      printf '%s\n' "$built"
    else
      echo "_No 'What was built' entry found in handoff-log.md — judge against the requirements directly._"
    fi
  } > "$out"

  added=$((added+1))
done

echo "Eval-set: $added added, $skipped skipped."
echo "Files in: $EVAL_DIR"
